const calculator = {
    add : function(a, b){
        console.log(a + b)
    },
    min : function(a, b){
        console.log(a - b)
    },
    div : function(a, b){
        console.log(a / b)
    },
    powerof : function(a, b){
        console.log(a ** b)
    }
};

calculator.add(1, 2);
calculator.min(1, 2);
calculator.div(1, 2);
calculator.powerof(1, 2);
